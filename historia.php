<div id="historia" style="width=80%;">
Tras el descubrimiento del sepulcro del Apóstol Santiago en la actual Compostela a principios del siglo IX, 
el lugar se transforma en destino de peregrinación para millones de europeos durante la Edad Media. 
</br>Los nobles y reyes de los pequeños reinos cristianos de la península Ibérica favorecieron el desarrollo 
de la ruta de peregrinación, que se convirtió en el cordón umbilical con el resto de la Europa cristiana. 
La marea de peregrinos era tal, que algunos años superaban en número a la población de las principales ciudades del Camino.
</br>Esta eclosión fue precedida por el cambio y afianzamiento de trazado que tuvo lugar a principios del siglo XI, 
cuando Sancho el Mayor de Navarra y Alfonso VI de León deciden que la ruta de peregrinación principal discurra por 
las tierras que estaban siendo reconquistadas. 
</br>El nuevo Camino, además de ruta de peregrinación, se convierte en un torrente de cultura, arte, comercio y 
repobladores, que llegan de toda Europa. A partir del siglo XVI, la cifra se reduce y cae casi hasta su desaparición 
tras la desamortización de Mendizábal (1835) pues supuso la extinción de la hospitalidad que se ejercía hasta aquel entonces. 
</br>Algunos años, los peregrinos se pueden contar con los dedos de las manos.
A partir de los años cuarenta del siglo XX resurge el interés por el Camino, gracias al empeño de anónimos 
amantes del Camino, un nuevo interés de las administraciones, las visitas del Papa a Santiago en los años ochenta
 y el renovado esfuerzo de la Iglesia, el desarrollo de múltiples asociaciones y cofradías y la declaración de 
 Patrimonio de la Humanidad. Los peregrinos del siglo XXI, hartos de la sobrecarga materialista, 
 busca un refugio ante el mundo que nos asfixia, y se pone en marcha hacia Santiago. 
</br>El año 2015 llegaron a Santiago 262.515 peregrinos de un centenar de países a pie, en bicicleta o a caballo, 
más una marea humana diaria imposible de contar de peregrinos en vehículos particulares o autobuses. 
En los últimos años, además, la cifra aumenta en torno a un 10% respecto al año anterior. 
</br>Peregrinos que buscan y encuentran en el Camino abnegación, esfuerzo, espiritualidad, aventura, solidaridad, 
compañerismo, sudor, lágrimas y alegrías y mucho camino por andar.
